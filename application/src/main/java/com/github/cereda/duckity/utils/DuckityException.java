package com.github.cereda.duckity.utils;

/**
 * The exception model.
 *
 * @author Paulo Roberto Massa Cereda
 * @version 1.1
 * @since 1.0
 */
public class DuckityException extends Exception {

    public DuckityException() {
        super();
    }

    public DuckityException(String message) {
        super(message);
    }

    public DuckityException(String message, Throwable cause) {
        super(message, cause);
    }

    public DuckityException(Throwable cause) {
        super(cause);
    }

    public DuckityException(String message, Throwable cause,
            boolean suppress, boolean stack) {
        super(message, cause, suppress, stack);
    }

}
